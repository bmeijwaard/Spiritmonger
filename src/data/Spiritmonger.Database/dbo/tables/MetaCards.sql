CREATE TABLE [dbo].[MetaCards]
(
	[Id] UNIQUEIDENTIFIER NOT NULL, 
	[Name] NVARCHAR(256) NOT NULL UNIQUE,
	[MetaType] TINYINT NOT NULL,
	[CardAdvantage] INT NOT NULL,
	[CMC] INT NOT NULL,
	[ManaCost] NVARCHAR(64) NULL,
	[Mana] NVARCHAR(64) NULL,
	[IsInitial] BIT NOT NULL,
	CONSTRAINT [PK_MetaCards] PRIMARY KEY CLUSTERED ([Id])
)
GO